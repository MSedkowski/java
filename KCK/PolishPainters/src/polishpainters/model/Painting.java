
package polishpainters.model;


public class Painting {

   private String fileSource;
   private String paintingName;
   private String paintingData;
   private String paintingLocation;

    public String getFileSource() {
        return fileSource;
    }

    public void setFileSource(String fileSource) {
        this.fileSource = fileSource;
    }

    public String getPaintingName() {
        return paintingName;
    }

    public void setPaintingName(String paintingName) {
        this.paintingName = paintingName;
    }

    public String getPaintingData() {
        return paintingData;
    }

    public void setPaintingData(String paintingDate) {
        this.paintingData = paintingDate;
    }

    public String getPaintingLocation() {
        return paintingLocation;
    }

    public void setPaintingLocation(String paintingLocation) {
        this.paintingLocation = paintingLocation;
    }
    
    
    
}
